package rs.model;

public class ChapterModel {

	private String title;		//章节标题
	private String url;			//章节地址
	
	public void setTitle(String i) {
		title = i;
	}
    public String  getTitle() {
		return title;
	}
    
    public void setUrl(String i) {
		url = i;
	}
    public String  getUrl() {
		return url;
	}
}
